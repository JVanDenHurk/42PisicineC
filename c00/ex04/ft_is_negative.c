#include <unistd.h>

void	ft_is_negative(int n)
{
	char	_n;
	char	p;

	_n = 'N';
	p = 'P';
	if (n < 0)
	{		
		write(1, &_n, 1);
	}
	else if (n >= 0)
	{
		write(1, &p, 1);
	}
}
